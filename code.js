$("#m11").click(function(){
    var x = document.getElementById("m11");
    
    if(x.innerHTML === "＋MORE"){
        x.innerHTML = "ーLESS";
        $(".pers11").fadeToggle(1000);
    }
    else if(x.innerHTML === "ーLESS"){
        x.innerHTML = "＋MORE";
        $(".pers11").hide();
    }
});
